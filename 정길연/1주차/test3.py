def maxLength(a, k):
    visited = [0] * a.pop(0)
    for i in range(len(a)):
        if a[i] >= k :
            visited[i] = 1
    ans = 0
    
    while True:
        if len(a) == 0 : 
            return ans
        if k >= a[0] :
            k -= a[0]
            ans += 1
            a.pop(0)
        else:
            break
    return ans


